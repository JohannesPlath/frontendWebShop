export class User {

  constructor(userID, firstname, familyName, address, city, state, zip, country, mail, password) {
    this.userID = userID;
    this.firstname = firstname;
    this.familyName = familyName;
    this.address = address;
    this.city = city;
    this.state = state;
    this.zip = zip;
    this.country = country;
    this.mail = mail;
    this.password = password;
  }
}
